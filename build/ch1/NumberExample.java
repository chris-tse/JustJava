public class NumberExample {
	public static void main(String[] args) {
		
		// declare an integer variable myInt with a value of 20
		int myInt = 20;
		// declare a floating point variable myFloat with a value of 3.14
		double myFloat = 3.14;

		// Print out each of the variables
		System.out.println(myInt);
		System.out.println(myFloat);
	}
}
